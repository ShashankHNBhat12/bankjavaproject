package pojoclasses;

public class Bank {
	int acccno;
	int amount;
	String acctype;
	public int getAcccno() {
		return acccno;
	}
	public void setAcccno(int acccno) {
		this.acccno = acccno;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
	public String getAcctype() {
		return acctype;
	}
	public void setAcctype(String acctype) {
		this.acctype = acctype;
	}
	

}
